import {History} from "../navigation/history.js";
import {Map} from "coreutil_v1";

export class State {

    constructor() {

        this.stateListenerMap = new Map();
    }

    recordState(newPath) {
        var url = History.getUrl();
        // Push current url to browser history
        if(!(url.getPath(0) === newPath)) {
            this.setUrl(url);
        }
    }

    /**
     * Sets a new state
     * 
     * @param url 
     */
    setUrl(url) {
        History.pushUrl(url,"",{});
    }
}