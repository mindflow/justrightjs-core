import { Url } from "../util/url";

export class History {

    static pushUrl(url,title,stateObject) {
        window.history.pushState(stateObject, title, url.toString());
    }

    static getUrl() {
        return new Url(window.location.href);
    }

    static loadUrl(url) {
        window.location = url.toString();
    }
}